from typing import List
from pydantic import BaseModel, Field

class SingleQuery(BaseModel):
    user_query: str = Field(description="Текст одного реалистичного запроса")

class SyntheticBatch(BaseModel):
    queries: List[SingleQuery]

class JudgeResponse(BaseModel):
    is_valid: bool = Field(description="Соответствует ли запрос роли, категории и ожидаемому выходу?")
    score: float = Field(description="Оценка качества от 0 до 1")
    critique: str = Field(description="Причина, если запрос не прошел проверку")

